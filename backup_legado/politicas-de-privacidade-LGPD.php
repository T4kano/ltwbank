				<p>
					• Informações sobre o seu computador, incluindo seu endereço IP, localização geográfica, tipo e versão do navegador e sistema operacional;<br>
					• Informações sobre suas visitas e uso deste aplicativo e site, incluindo fonte de referência, duração da visita, visualizações de página e caminhos de navegação no aplicativo e site;<br>
