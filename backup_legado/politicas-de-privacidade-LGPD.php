					• Informações que você digita ao criar um perfil em nosso aplicativo e site – por exemplo, seu nome, fotos de perfil, sexo, data de nascimento, status de relacionamento, interesses e hobbies, informações educacionais e de emprego;<br>
					• Informações, como seu nome e endereço de e-mail, que você digita para configurar assinaturas de nossos e-mails e/ou newsletters;<br>
					• Informações que você digita durante o uso dos serviços em nosso aplicativo e site;<br>
					• Informações geradas ao usar nosso aplicativo e site, incluindo quando, com que frequência e em que circunstâncias você o utiliza;<br>
					• Informações relacionadas a tudo o que você compra, serviços que usa ou transações que realiza através do nosso aplicativo e site, incluindo nome, endereço, número de telefone, endereço de e-mail e dados do cartão de crédito;<br>
